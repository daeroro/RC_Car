typedef struct t_dist
{
	int dof;

	float t_score;
	float sam_mean;
	float pop_mean;
	float sam_var;
	float sam_std_dev;

	float confidence;
	float conf_bound;

	float low_bound;
	float high_bound;

	int sam_cnt;
} td;

void init_t_dist(td *t, int cnt, float sam_mean, float sam_var)
{
	t->sam_cnt = cnt;
	t->sam_mean = sam_mean;
	t->sam_var = sam_var;

	t->dof = cnt - 1;
}

void set_confidence(td *t, float confidence)
{
	t->confidence = confidence / 100.0;
}

void calc_bound(td *t)
{
	t->conf_bound = (1.0 - t->confidence) / 2.0;
}

