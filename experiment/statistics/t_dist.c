void calc_lh_bound(td *t)
{
	t->low_bound = t->sam_mean - (t->t_score * sqrt(t->sam_var / (float)t->sam_cnt));
	t->high_bound = t->sam_mean + (t->t_score * sqrt(t->sam_var / (float)t->sam_cnt));
}

void calc_tscore(td *t)
{
	calc_bound(t);
	find_tscore(t);
	calc_lh_bound(t);
}

